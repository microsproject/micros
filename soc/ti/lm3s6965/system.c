#include <stdint.h>
/*--------------------------------------------------------------------------------------------------------------------*/
#define UART0_DR (*(volatile uint32_t*)0x4000C000)
#define UART0_FR (*(volatile uint32_t*)0x4000C018)
#define UART_FR_TXFF (1 << 5)
/*--------------------------------------------------------------------------------------------------------------------*/
void stdout_putc(char c) {
    while (UART0_FR & UART_FR_TXFF)
        ;
    UART0_DR = c;
}
